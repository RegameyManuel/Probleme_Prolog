/**************************************
 * Règles générales
 **************************************/

% X est le père de Y si X est un homme et X est parent de Y
pere(X, Y) :-
    homme(X),
    parent(X, Y).

% X est la mère de Y si X est une femme et X est parent de Y
mere(X, Y) :-
    femme(X),
    parent(X, Y).

% Les grands-parents : X est grand-parent de Y si X est parent de Z et Z est parent de Y
grandparent(X, Y) :-
    parent(X, Z),
    parent(Z, Y).

% X est le grand-père de Y s'il est grand-parent de Y et c'est un homme
grandpere(X, Y) :-
    grandparent(X, Y),
    homme(X).

% X est la grand-mère de Y s'il est grand-parent de Y et c'est une femme
grandmere(X, Y) :-
    grandparent(X, Y),
    femme(X).

% Frères et soeurs : X et Y sont frères/soeurs s'ils partagent un parent commun et sont distincts
frere_soeur(X, Y) :-
    parent(Z, X),
    parent(Z, Y),
    X \= Y,
    X @< Y.

% On peut affiner en définissant frere et soeur spécifiquement :
frere(X, Y) :-
    frere_soeur(X, Y),
    homme(X).

soeur(X, Y) :-
    frere_soeur(X, Y),
    femme(X).

% Oncle et tante : X est oncle ou tante de Y si X est frère/soeur d'un parent de Y
oncle(X, Y) :-
    homme(X),
    parent(Z, Y),
    frere_soeur(X, Z).

tante(X, Y) :-
    femme(X),
    parent(Z, Y),
    frere_soeur(X, Z).

% Cousins : X et Y sont cousins/cousines si leurs parents sont frères/soeurs
cousin_cousine(X, Y) :-
    parent(Px, X),
    parent(Py, Y),
    frere_soeur(Px, Py),
    X \= Y.


% De même, on peut distinguer cousin (masculin) et cousine (féminin) :
cousin(X, Y) :-
    cousin_cousine(X, Y),
    homme(X),
    X \= Y.

cousine(X, Y) :-
    cousin_cousine(X, Y),
    femme(X),
    X \= Y.

% Ancêtre : X est un ancêtre de Y si X est parent de Y ou s'il est parent de quelqu'un qui est ancêtre de Y
ancetre(X, Y) :-
    parent(X, Y).
ancetre(X, Y) :-
    parent(X, Z),
    ancetre(Z, Y).